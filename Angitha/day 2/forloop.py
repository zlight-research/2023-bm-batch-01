"""Use FOR and WHILE loop to display the below manner."""
"""EVEN NUMBER"""
print("Even num:")
for i in range(10,0,-2):
    if(i%2==0):"""In even number we get i%2===0"""
    print(i)
"""ODD NUMBER"""
print(" Odd num:")
for i in range(9,0,-2):
    if(i%2!=0):"""In odd number we get i%2===0"""
    print(i)